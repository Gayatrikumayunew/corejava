public class operator1 {
    public static void main(String[] args) {
        int a=5;
        int b=5;
        int ans=a+b;
        System.out.println(ans);
        ans=a-b;
        System.out.println(ans);
        System.out.println(a+(a-b));
        System.out.println(a-(a+b));
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a*b);
    }
    
}
